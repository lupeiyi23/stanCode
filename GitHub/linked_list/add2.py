import sys


class ListNode:
    def __init__(self, data=0, pointer=None):
        self.val = data
        self.next = pointer


def add_2_numbers(l1: ListNode, l2: ListNode) -> ListNode:
    cur = l1
    power = 0
    l1_sum = 0
    while cur is not None:
        l1_sum += cur.val * (10 ** power)
        power += 1
        cur = cur.next
    cur = l2
    power = 0
    l2_sum = 0
    while cur is not None:
        l2_sum += cur.val * (10 ** power)
        power += 1
        cur = cur.next
    ans_sum = l1_sum + l2_sum
    ans = ListNode(ans_sum % 10, None)
    ans_sum = ans_sum // 10
    cur = ans
    while True:
        if ans_sum < 1:
            break
        else:
            cur.next = ListNode(ans_sum % 10, None)
            ans_sum = ans_sum // 10
            cur = cur.next
    return ans


